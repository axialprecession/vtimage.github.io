export const getResourcesByCategory = (category: string) => {
  return resources.filter(r => {
    const normSearch = category.toLowerCase().trim();
    const normType = r.type.toLowerCase().trim();
    
    // Robust mapping for categories to handle UI labels vs internal types
    if (normSearch === 'shelter' || normSearch === 'emergency shelters') return normType === 'shelter';
    if (normSearch === 'legal aid') return normType === 'legal aid';
    if (normSearch === 'food bank' || normSearch === 'food banks') return normType === 'food bank';
    if (normSearch === 'mental health') return normType === 'mental health';
    if (normSearch === 'domestic violence') return normType === 'domestic violence';
    if (normSearch === 'chinese services' || normSearch === 'chinese speaking support') return normType === 'chinese services';
    if (normSearch === 'addiction recovery') return normType === 'addiction recovery';
    
    return normType === normSearch;
  });
};

export const searchResources = (query: string): Resource[] => {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return resources.filter(r => 
    r.name.toLowerCase().includes(q) ||
    r.nameZhTW?.toLowerCase().includes(q) ||
    r.nameZhCN?.toLowerCase().includes(q) ||
    r.type.toLowerCase().includes(q) ||
    r.location.toLowerCase().includes(q) ||
    r.description.toLowerCase().includes(q) ||
    r.descriptionZhTW?.toLowerCase().includes(q) ||
    r.descriptionZhCN?.toLowerCase().includes(q)
  );
};

export const getSearchSuggestions = (query: string): string[] => {
   const q = query.toLowerCase().trim();
   if (!q) return [];
   const suggestions = new Set<string>();
   
   resources.forEach(r => {
     if (r.name.toLowerCase().includes(q)) suggestions.add(r.name);
     if (r.nameZhTW && r.nameZhTW.includes(q)) suggestions.add(r.nameZhTW);
     if (r.type.toLowerCase().includes(q)) suggestions.add(r.type);
     if (r.location.toLowerCase().includes(q)) {
       // Extract city roughly
       const city = r.location.split(',')[1]?.trim();
       if (city) suggestions.add(city);
     }
   });
   return Array.from(suggestions).slice(0, 5);
};
